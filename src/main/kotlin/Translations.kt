fun strings(lang: String): AppStrings = if (lang == "ar") AR else EN

fun Zone.label(lang: String): String = when (this) {
    Zone.HOOD   -> if (lang == "ar") AR.zoneHood   else EN.zoneHood
    Zone.BODY   -> if (lang == "ar") AR.zoneBody   else EN.zoneBody
    Zone.WHEELS -> if (lang == "ar") AR.zoneWheels else EN.zoneWheels
    Zone.BRAKES -> if (lang == "ar") AR.zoneBrakes else EN.zoneBrakes
    Zone.LIGHTS -> if (lang == "ar") AR.zoneLights else EN.zoneLights
    Zone.WIPERS -> if (lang == "ar") AR.zoneWipers else EN.zoneWipers
}
